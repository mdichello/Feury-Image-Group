from odoo import api, fields, models


class MaterialType(models.Model):
    _name = 'material.type'
    _description = 'Material Type'

    _sql_constraints = [
        ('material_type_uniq', 'unique(name)', 'No duplicate are allowed for material names.')
    ]

    name = fields.Char(
        string='Name',
        required=True,
        unique=True
    )

    description = fields.Char(
        string='Description',
        required=True,
        unique=True
    )

    active = fields.Boolean(
        string='Active',
        default=True
    )

